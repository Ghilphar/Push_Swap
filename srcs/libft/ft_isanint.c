int		ft_isanint(char *str)
{
	int			i;

	i = 0;
	if (!str)
		return(0);
	while (str[i])
	{
		if ((str[i] < 48 || str[i] > 57) && str[i] != 45)
			return (0);
		i++;
		if (i > 11)
			return (0);
	}
	if (i == 11 && (str[0] != '-' || str[1] > '2' || str[10] > '8'))
		return (0);
	if (i == 10 && (str[0] > '2' || str[9] > '7'))
		return (0);
	return (1);
}